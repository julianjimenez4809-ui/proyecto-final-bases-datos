-- -------------------------------------------------------------
-- DELETE — Limpieza de registros (soft delete preferido)
-- -------------------------------------------------------------

-- Eliminar pedidos cancelados sin pago aprobado (registros de prueba)
DELETE FROM pago
WHERE pedido_id IN (
    SELECT p.pedido_id FROM pedido p
    LEFT JOIN pago pa ON p.pedido_id = pa.pedido_id
    WHERE p.estado = 'cancelado' AND pa.estado = 'reembolsado'
);

DELETE FROM pedido
WHERE estado = 'cancelado'
  AND pedido_id NOT IN (SELECT pedido_id FROM pago);
